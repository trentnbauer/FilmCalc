let currentLocale = localStorage.getItem('locale') && STRINGS[localStorage.getItem('locale')]
    ? localStorage.getItem('locale')
    : 'en';

function setLocale(locale) {
    if (STRINGS[locale]) currentLocale = locale;
}

// t('key', { n: 2 }) substitutes {n} placeholders in the dictionary string.
// Falls back to the English string (then the raw key) if a locale is
// missing a translation, so a partial locale never renders blank text.
function t(key, params) {
    const dict = STRINGS[currentLocale] || {};
    let str = dict[key] ?? STRINGS.en[key] ?? key;
    if (params) {
        for (const [name, value] of Object.entries(params)) {
            str = str.replaceAll(`{${name}}`, value);
        }
    }
    return str;
}

// Static markup hydration: elements tagged with data-i18n get their
// textContent set from STRINGS; data-i18n-placeholder / data-i18n-aria-label /
// data-i18n-title do the same for those attributes. Call once on DOMContentLoaded
// (and again if new markup is injected after locale-dependent init).
function applyI18n(root = document) {
    root.querySelectorAll('[data-i18n]').forEach((el) => {
        el.textContent = t(el.getAttribute('data-i18n'));
    });
    root.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
        el.setAttribute('placeholder', t(el.getAttribute('data-i18n-placeholder')));
    });
    root.querySelectorAll('[data-i18n-aria-label]').forEach((el) => {
        el.setAttribute('aria-label', t(el.getAttribute('data-i18n-aria-label')));
    });
    root.querySelectorAll('[data-i18n-title]').forEach((el) => {
        el.setAttribute('title', t(el.getAttribute('data-i18n-title')));
    });
}

document.addEventListener('DOMContentLoaded', () => {
    const languageSelect = document.getElementById('languageSelect');
    if (languageSelect) {
        languageSelect.value = currentLocale;
        languageSelect.addEventListener('change', (e) => {
            setLocale(e.target.value);
            localStorage.setItem('locale', currentLocale);
            applyI18n();
        });
    }
    applyI18n();
});
